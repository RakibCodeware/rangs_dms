    //LOAD SALES SUMMARY CHALLAN WISE
    private void fnLoadData()
    {
        //s = WebConfigurationManager.ConnectionStrings["ConnectionString"].ConnectionString;
        //con = new SqlConnection(s);
        SqlConnection con = DBConnection.GetConnection();
        con.Open();

        string sSql = "";

        sSql = "SELECT CONVERT(VARCHAR(10), dbo.tbDeposit.DepositDate, 103) AS DepositDate, SUM(DepositAmnt) AS DAmnt";
        //sSql = sSql + " FROM  dbo.tbDeposit";
        sSql = sSql + " FROM dbo.tbDeposit INNER JOIN dbo.Entity ON dbo.tbDeposit.EID = dbo.Entity.EID";
        sSql = sSql + " WHERE (month(DepositDate) = '" + DateTime.Now.Month.ToString() + "')";
        sSql = sSql + " AND (year(DepositDate) = '" + DateTime.Now.Year.ToString() + "')";
        sSql = sSql + " AND (dbo.Entity.EntityType = 'Dealer')";
        sSql = sSql + " GROUP BY DepositDate";
        sSql = sSql + " ORDER BY DepositDate";

        SqlCommand cmd = new SqlCommand(sSql, con);
        //OleDbDataReader dr = cmd.ExecuteReader();

        DataSet ds = new DataSet();
        SqlDataAdapter da = new SqlDataAdapter(cmd);
        da.Fill(ds);

        GridView1.DataSource = ds;
        GridView1.DataBind();
        //dr.Close();
        con.Close();

    }

